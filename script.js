const form = document.getElementById('calculator-form');
const resultsSection = document.getElementById('results');
const attendanceInput = document.getElementById('attendance');
const classesInput = document.getElementById('classes-conducted');
const whatIfSlider = document.getElementById('what-if-slider');
const sliderValueDisplay = document.getElementById('slider-value');

let currentAttendance = null;
let currentClassesConducted = null;

form.addEventListener('submit', (e) => {
  e.preventDefault();
  gtag('event', 'calculate_clicked');
  calculateResults();
});
whatIfSlider.addEventListener('input', (e) => {
    sliderValueDisplay.textContent = e.target.value;
    updateWhatIfResults();
});

function calculateResults() {
    const attendance = parseFloat(attendanceInput.value);
    const classesConducted = parseInt(classesInput.value);
    if (isNaN(attendance) || isNaN(classesConducted) || attendance < 0 || attendance > 100 || classesConducted <= 0) {
        alert('Please enter valid values');
        return;
    }
    currentAttendance = attendance;
    currentClassesConducted = classesConducted;

    const classesAttended = Math.round((attendance / 100) * classesConducted);
    const classesMissed = classesConducted - classesAttended;
    const statusTier = getStatusTier(attendance);

    updateStatusBar(attendance, statusTier);
    updateCardColors(statusTier);
    fetchGif(statusTier);

    document.getElementById('classes-missed').textContent = classesMissed;
    updateSafeBunks(attendance, classesAttended, classesConducted, statusTier);
    updateRecoveryCount(attendance, classesAttended, classesConducted);
    updateNextBunkResult(classesAttended, classesConducted);

    whatIfSlider.value = 15;
    sliderValueDisplay.textContent = 15;
    updateWhatIfResults();

    resultsSection.classList.remove('hidden');
    resultsSection.scrollIntoView({ behavior: 'smooth', block: 'start' });
}

function getStatusTier(attendance) {
    if (attendance >= 75) return 'safe';
    if (attendance >= 60) return 'medical';
    return 'danger';
}

function updateStatusBar(attendance, tier) {
    const statusBar = document.getElementById('status-bar');
    statusBar.className = `status-bar ${tier}`;
    document.getElementById('status-percentage').textContent = attendance.toFixed(2) + '%';
}

function updateCardColors(tier) {
    const cardBunks = document.getElementById('card-bunks');
    const cardRecovery = document.getElementById('card-recovery');
    cardBunks.className = 'result-card';
    cardRecovery.className = 'result-card';
    if (tier === 'safe') {
        cardBunks.classList.add('safe-card');
    } else if (tier === 'medical') {
        cardBunks.classList.add('medical-card');
        cardRecovery.classList.add('medical-card');
    } else {
        cardBunks.classList.add('danger-card');
        cardRecovery.classList.add('danger-card');
    }
}

function updateSafeBunks(attendance, classesAttended, classesConducted, tier) {
    const el = document.getElementById('safe-bunks');
    if (tier === 'safe') {
        let bunks = 0, ta = classesAttended, tc = classesConducted;
        while (true) { tc++; if ((ta / tc) * 100 < 75) break; bunks++; }
        el.textContent = bunks > 0 ? bunks : '0';
    } else if (tier === 'medical') {
        let bunks = 0, ta = classesAttended, tc = classesConducted;
        while (true) { tc++; if ((ta / tc) * 100 < 60) break; bunks++; }
        el.textContent = bunks > 0 ? `${bunks} (to 60%)` : '0';
    } else {
        el.textContent = 'None';
    }
}

function updateRecoveryCount(attendance, classesAttended, classesConducted) {
    const el = document.getElementById('recovery-count');
    if (attendance >= 75) { el.textContent = '—'; return; }
    if (attendance < 60) {
        const to60 = calculateToReach(classesAttended, classesConducted, 0.60);
        const to75 = calculateToReach(classesAttended + to60, classesConducted + to60, 0.75);
        el.textContent = to60 + to75;
    } else {
        el.textContent = calculateToReach(classesAttended, classesConducted, 0.75);
    }
}

function calculateToReach(attended, conducted, target) {
    let n = 0, a = attended, c = conducted;
    while ((a / c) < target) { n++; a++; c++; }
    return n;
}

function updateNextBunkResult(classesAttended, classesConducted) {
    const pct = (classesAttended / (classesConducted + 1)) * 100;
    document.getElementById('next-bunk').textContent = pct.toFixed(2) + '%';
}

function updateWhatIfResults() {
    if (currentAttendance === null) return;
    const n = parseInt(whatIfSlider.value);
    const attended = Math.round((currentAttendance / 100) * currentClassesConducted);
    const newPct = ((attended + n) / (currentClassesConducted + n)) * 100;
    const tier = getStatusTier(newPct);
    document.getElementById('what-if-attendance').textContent = newPct.toFixed(2) + '%';
    const statusEl = document.getElementById('what-if-status');
    statusEl.className = `result-status ${tier}`;
    statusEl.textContent = { safe: 'Safe ✓', medical: 'Med Zone', danger: 'Danger' }[tier];
}

function fetchGif(tier) {
    const data = GIF_DATA[tier];
    const caption = data.captions[Math.floor(Math.random() * data.captions.length)];
    const gif = data.gifs[Math.floor(Math.random() * data.gifs.length)];
    document.getElementById('meme-caption').textContent = caption;
    document.getElementById('status-gif').src = gif;
}
